// Tipos de dados
// Números | Strings | Booleanos | Valores Indefinidos

// 1. Number e Operadores aritméticos

console.log(1 + 2); // 3
console.log(1 - 2); // -1
console.log(1 / 2); // 0.5
console.log(1 * 2); // 2

console.log(5 + 3 * 4)
console.log((5 + 3) * 4)


// 2. String

console.log('Ola Mundo!');
console.log("Ola Mundo!");
console.log(`Ola Mundo!`);

console.log(`Um texto com quebra de 
    linha`);

console.log("Um texto com quebra de \n linha");

console.log("Aspas simples ' ' ");
console.log('Aspas duplas " " ');

console.log(`"Aspas duplas" e 'Aspas simples'`);
console.log("Aspas duplas \" \" ");

console.log("5");

console.log('Tiago' + ' Lima');

console.log("1" + 1); // 11
console.log(1 + "1"); // 11

console.log(+"1" + 1 ); // 2 (número)
console.log(Number("1") + 1 ); // 2 (número)

// 3. Boolean e operadores de comparação

console.log(true);
console.log(false);

console.log(1 > 2);
console.log(1 < 2);
console.log(1 >= 2);
console.log(1 <= 2);

console.log("2" == 2);
console.log("2" === 2);
console.log("2" === "2");

console.log("2" != 2);
console.log("2" !== 2);

// 4. Valores indefinidos e Números especiais

console.log(-1/0);
console.log(1/0);

console.log(5/"banana");

console.log(typeof 5);

console.log(undefined);
console.log(null);


